#!/usr/bin/env node
/**
 * generate-seo-layouts.js
 *
 * Generates a thin layout.tsx (metadata + JSON-LD) for every tool route
 * from the TOOL_SEO table in src/lib/seo.ts. Idempotent — safe to re-run
 * after adding a new tool; existing generated layouts are overwritten.
 *
 * Run: node scripts/generate-seo-layouts.js
 */

const fs = require('fs');
const path = require('path');

// key → app route directory (relative to src/app)
const ROUTES = {
    'pdf-merge': 'pdf/merge',
    'pdf-split': 'pdf/split',
    'pdf-compress': 'pdf/compress',
    'pdf-to-word': 'pdf/pdf-to-word',
    'pdf-to-excel': 'pdf/pdf-to-excel',
    'office-to-pdf': 'pdf/office-to-pdf',
    'pdf-editor': 'pdf/editor',
    'pdf-rotate': 'pdf/rotate',
    'pdf-remove-pages': 'pdf/remove-pages',
    'pdf-organize': 'pdf/organize',
    'pdf-watermark': 'pdf/watermark',
    'pdf-metadata': 'pdf/metadata',
    'pdf-page-numbers': 'pdf/page-numbers',
    'pdf-flatten': 'pdf/flatten',
    'pdf-protect': 'pdf/protect',
    'pdf-unlock': 'pdf/unlock',
    'pdf-repair': 'pdf/repair',
    'pdf-pdfa': 'pdf/pdfa',
    'pdf-redact': 'pdf/redact',
    'pdf-ocr': 'pdf/ocr',
    'pdf-resize-pages': 'pdf/resize-pages',
    'pdf-scan': 'pdf/scan',
    'image-compress': 'image/compress',
    'image-convert': 'image/convert',
    'image-resize': 'image/resize',
    'image-batch': 'image/batch',
    'image-exif': 'image/exif',
    'video-convert': 'video/convert',
    'video-trim': 'video/trim',
    'video-extract-audio': 'video/extract-audio',
    'ocr': 'ocr',
    'zip-create': 'zip',
    'zip-extract': 'unzip',
    'qr-generator': 'qr',
    'mermaid-to-flowchart': 'mermaid-to-flowchart',
    'hub-pdf': 'pdf',
    'hub-image': 'image',
    'hub-video': 'video',
};

const appDir = path.join(__dirname, '..', 'src', 'app');
let written = 0;

for (const [key, route] of Object.entries(ROUTES)) {
    const dir = path.join(appDir, route);
    if (!fs.existsSync(dir)) {
        console.warn(`  ⚠  route dir missing, skipped: ${route}`);
        continue;
    }
    const content = `// AUTO-GENERATED by scripts/generate-seo-layouts.js — edit src/lib/seo.ts instead.
import type { Metadata } from 'next';
import { toolMetadata } from '@/lib/seo';
import { ToolJsonLd } from '@/components/seo/ToolJsonLd';

export const metadata: Metadata = toolMetadata('${key}');

export default function SeoLayout({ children }: { children: React.ReactNode }) {
    return (
        <>
            {children}
            <ToolJsonLd toolKey="${key}" />
        </>
    );
}
`;
    fs.writeFileSync(path.join(dir, 'layout.tsx'), content);
    written++;
    console.log(`  ✓  ${route}/layout.tsx`);
}

console.log(`\n${written} SEO layouts written.`);
